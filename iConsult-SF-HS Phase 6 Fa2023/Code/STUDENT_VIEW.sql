CREATE VIEW student_view AS
SELECT 
    p.sf_contact_id,
    p.uid,
    p.first_name,
    p.middle_name,
    p.last_name,
    p.name_prefix,
    p.name_suffix,
    p.email,
    p.alternate_email,
    p.gender,
    p.country_of_citizenship,
    p.citz_visa,
    p.has_dual_citizenship,
    p.dual_citizenship_country,
    p.ethnicity_1,
    p.ethnicity_2,
    p.contact_hispanic,
    p.linkedin_url,
    p.primary_UG_school,
    p.mobile_phone,
    p.home_phone_formatted,
    p.wechat_id,
    p.military_status,
    p.military_branch,
    p.first_generation_students,
    d.hs_student_id,
    d.program,
    d.program_type,
    d.start_term,
    d.last_registered_term,
    d.graduation_term,
    d.graduation_clearance,
    d.undergraduate_major,
    d.undergraduate_major_2,
    d.transfer_type,
    d.cohort,
    d.campus,
    d.level_of_study,
    d.quest_program,
    d.four_plus_one_grad_program,
    d.shady_grove_ind,
	d.contact_company,
    d.contact_title,
    d.months_worked,
    d.rhs_company_sponsored,
    d.gs_admissions_status,
    d.deposit_fee_transaction_date
FROM
    sf_student_profile p
JOIN
    sf_student_degree d ON p.sf_contact_id = d.sf_contact_id;
